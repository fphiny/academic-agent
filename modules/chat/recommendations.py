def get_openai_client() -> OpenAI:
    global _openai_client

    if _openai_client is None:
        if not OPENAI_API_KEY:
            raise RuntimeError("OPENAI_API_KEY is not set.")
        _openai_client = OpenAI(api_key=OPENAI_API_KEY)

    return _openai_client


def _safe_json_loads(text: str) -> Dict:
    raw = (text or "").strip()

    if raw.startswith("```json"):
        raw = raw[7:].strip()
    elif raw.startswith("```"):
        raw = raw[3:].strip()

    if raw.endswith("```"):
        raw = raw[:-3].strip()

    return json.loads(raw)


def _prepare_student_payload(student_id: str, grade_df: pd.DataFrame, question: str) -> tuple[Dict, set[str]]:
    df = grade_df.copy()

    for col in ["년도", "학기", "과목명", "학점", "성적", "이수구분"]:
        if col not in df.columns:
            df[col] = None

    df["학점"] = pd.to_numeric(df["학점"], errors="coerce")

    completed_course_names = {
        str(name).strip()
        for name in df["과목명"].fillna("").astype(str).tolist()
        if str(name).strip()
    }

    course_rows = (
        df[["년도", "학기", "과목명", "학점", "성적", "이수구분"]]
        .fillna("")
        .to_dict(orient="records")
    )

    analysis_cache = load_cached_analysis(student_id)

    payload = {
        "student_id": student_id,
        "question": question,
        "completed_courses": course_rows[:150],
        "existing_analysis": analysis_cache if analysis_cache else {},
    }

    return payload, completed_course_names


