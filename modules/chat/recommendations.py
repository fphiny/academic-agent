def _request_explanation_once(payload: Dict) -> str:
    client = get_openai_client()

    user_prompt = f"""
학생 데이터:
{json.dumps(payload, ensure_ascii=False, indent=2)}
""".strip()

    response = client.responses.create(
        model=OPENAI_MODEL,
        input=[
            {"role": "system", "content": _build_explanation_system_prompt()},
            {"role": "user", "content": user_prompt},
        ],
    )

    return (response.output_text or "").strip()


def _stream_explanation_text(payload: Dict) -> Generator[str, None, None]:
    client = get_openai_client()

    user_prompt = f"""
학생 데이터:
{json.dumps(payload, ensure_ascii=False, indent=2)}
""".strip()

    stream = client.responses.create(
        model=OPENAI_MODEL,
        input=[
            {"role": "system", "content": _build_explanation_system_prompt()},
            {"role": "user", "content": user_prompt},
        ],
        stream=True,
    )

    for event in stream:
        event_type = getattr(event, "type", "")

        if event_type == "response.output_text.delta":
            delta = getattr(event, "delta", "")
            if delta:
                yield delta

        elif event_type == "error":
            message = getattr(event, "message", None) or str(event)
            raise RuntimeError(message)


def _request_recommended_courses_once(
    payload: Dict,
    completed_course_names: set[str],
    streamed_answer: str = "",
) -> list[Dict]:
    client = get_openai_client()

    request_payload = {
        **payload,
        "streamed_answer": streamed_answer,
    }

    user_prompt = f"""
학생 데이터:
{json.dumps(request_payload, ensure_ascii=False, indent=2)}
""".strip()

    response = client.responses.create(
        model=OPENAI_MODEL,
        input=[
            {"role": "system", "content": _build_course_json_system_prompt()},
            {"role": "user", "content": user_prompt},
        ],
    )

    result = _safe_json_loads(response.output_text or "{}")
    recommended_courses = result.get("recommended_courses", [])

    filtered_courses: list[Dict] = []

    for item in recommended_courses:
        course_name = str(item.get("course_name", "")).strip()
        if not course_name:
            continue
        if course_name in completed_course_names:
            continue

        reason = str(item.get("reason", "")).strip()

        try:
            priority = int(item.get("priority", len(filtered_courses) + 1))
        except Exception:
            priority = len(filtered_courses) + 1

        filtered_courses.append(
            {
                "course_name": course_name,
                "reason": reason,
                "priority": priority,
            }
        )

    filtered_courses.sort(key=lambda x: (x["priority"], x["course_name"]))
    return filtered_courses[:6]


def recommend_courses_for_career_question(
    student_id: str,
    grade_df: pd.DataFrame,
    question: str,
) -> dict:
    if grade_df is None or grade_df.empty:
        return {
            "question": question,
            "answer": "수강 데이터가 없어 과목 추천을 진행할 수 없습니다.",
            "recommended_courses": [],
        }

    payload, completed_course_names = _prepare_student_payload(
        student_id=student_id,
        grade_df=grade_df,
        question=question,
    )

    answer = _request_explanation_once(payload)
    recommended_courses = _request_recommended_courses_once(
        payload=payload,
        completed_course_names=completed_course_names,
        streamed_answer=answer,
    )

    return {
        "question": question,
        "answer": answer,
        "recommended_courses": recommended_courses,
    }


def stream_recommend_courses_for_career_question(
    student_id: str,
    grade_df: pd.DataFrame,
    question: str,
) -> Generator[Dict, None, None]:
    if grade_df is None or grade_df.empty:
        yield {
            "event": "courses",
            "data": {
                "answer": "수강 데이터가 없어 과목 추천을 진행할 수 없습니다.",
                "recommended_courses": [],
            },
        }
        yield {"event": "done", "data": {"done": True}}
        return

    payload, completed_course_names = _prepare_student_payload(
        student_id=student_id,
        grade_df=grade_df,
        question=question,
    )

    yield {
        "event": "meta",
        "data": {
            "type": "course_recommendation",
            "question": question,
        },
    }

    answer_parts: list[str] = []

    try:
        for delta in _stream_explanation_text(payload):
            if not delta:
                continue
            answer_parts.append(delta)
            yield {"event": "delta", "data": {"delta": delta}}
    except Exception:
        if not answer_parts:
            fallback_answer = _request_explanation_once(payload)
            if fallback_answer:
                answer_parts.append(fallback_answer)
                yield {"event": "delta", "data": {"delta": fallback_answer}}

    answer = "".join(answer_parts).strip()

    if not answer:
        answer = _request_explanation_once(payload)
        if answer:
            yield {"event": "delta", "data": {"delta": answer}}

    recommended_courses = _request_recommended_courses_once(
        payload=payload,
        completed_course_names=completed_course_names,
        streamed_answer=answer,
    )

    yield {
        "event": "courses",
        "data": {
            "answer": answer,
            "recommended_courses": recommended_courses,
        },
    }

    yield {"event": "done", "data": {"done": True}}